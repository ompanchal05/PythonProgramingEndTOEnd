#WAP FOR THE UNDERSTANDING THE LOGIC OF FOR LOOP AND WHILE LOOP

# For Loop Example
print("Using for loop:")

for i in range(1, 6):   # runs from 1 to 5
    print("Iteration", i)
# While Loop Example
print("\nUsing while loop:")

i = 1
while i <= 5:
    print("Iteration", i)
    i += 1   # same as i = i + 1
